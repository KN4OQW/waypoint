// Isolation + update-survival against a REAL renderer output, not just the sample.
func TestApplyOverrides_RealRender(t *testing.T) {
	m := fixture()
	base := m.RenderMMDVM()
	if got := parseOverrideDoc(base).render(); got != base {
		t.Fatalf("real render does not reserialize byte-identically")
	}
	frags := []Fragment{ParseFragment("10.conf", "[General]\nTimeout=600\n[Custom]\nFoo=bar")}
	first, report := ApplyOverrides("mmdvm", base, frags)
	second, _ := ApplyOverrides("mmdvm", base, frags)
	if first != second {
		t.Fatalf("real-render merge not idempotent")
	}
	if !strings.Contains(first, "Timeout=600") || !strings.Contains(first, "[Custom]\nFoo=bar") {
		t.Errorf("real-render merge missing edits:\n%s", first)
	}
	// Timeout changed + Custom.Foo added = 2 records.
	if len(report) != 2 {
		t.Errorf("expected 2 report records on real render, got %d: %+v", len(report), report)
	}
}

// End-to-end through the render pipeline: a fragment on disk under
// <OverridesDir>/mmdvm.d/ is merged into the written MMDVM-Host.ini, reported by
// Overrides(), and re-applying (an "update") produces byte-identical files.
func TestWriteFiles_Overrides(t *testing.T) {
	m := fixture()
	dir := t.TempDir()
	etc := t.TempDir()
	if err := os.MkdirAll(filepath.Join(dir, "mmdvm.d"), 0o755); err != nil {
		t.Fatal(err)
	}
	write(t, filepath.Join(dir, "mmdvm.d"), "10-local.conf", "[General]\nTimeout=999\n[LocalExtras]\nFoo=bar")

	paths := Paths{
		MMDVM: filepath.Join(etc, "MMDVM-Host.ini"), DMRGateway: filepath.Join(etc, "DMRGateway.ini"),
		YSFGateway: filepath.Join(etc, "YSFGateway.ini"), P25Gateway: filepath.Join(etc, "P25Gateway.ini"),
		NXDNGateway: filepath.Join(etc, "NXDNGateway.ini"), DStarGateway: filepath.Join(etc, "dstargateway.cfg"),
		M17Gateway: filepath.Join(etc, "M17Gateway.ini"), DAPNETGateway: filepath.Join(etc, "DAPNETGateway.ini"), OverridesDir: dir,
	}

	warnings, err := m.WriteFiles(paths)
	if err != nil {
		t.Fatal(err)
	}
	if len(warnings) != 0 {
		t.Errorf("unexpected warnings: %v", warnings)
	}
	got, err := os.ReadFile(paths.MMDVM)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(got), "Timeout=999") || !strings.Contains(string(got), "[LocalExtras]\nFoo=bar") {
		t.Errorf("override not merged into written file:\n%s", got)
	}

	// Overrides() must report the same edits without writing.
	applied, _, err := m.Overrides(paths)
	if err != nil {
		t.Fatal(err)
	}
	if len(applied) != 2 {
		t.Fatalf("Overrides() = %d records, want 2: %+v", len(applied), applied)
	}
	for _, a := range applied {
		if a.Daemon != "mmdvm" || a.Source != "10-local.conf" {
			t.Errorf("record has wrong provenance: %+v", a)
		}
	}

	// "Apply an update": re-render + re-merge over the same store and fragments ⇒
	// byte-identical file (the #2 acceptance).
	if _, err := m.WriteFiles(paths); err != nil {
		t.Fatal(err)
	}
	got2, _ := os.ReadFile(paths.MMDVM)
	if string(got) != string(got2) {
		t.Errorf("re-apply not byte-identical (update-survival):\n--- first ---\n%s\n--- second ---\n%s", got, got2)
	}
}

// With no OverridesDir the pipeline emits the pure render (inertness at the
// pipeline level, matching tests that pass Paths without an override root).
func TestWriteFiles_NoOverridesDir(t *testing.T) {
	m := fixture()
	etc := t.TempDir()
	paths := Paths{
		MMDVM: filepath.Join(etc, "MMDVM-Host.ini"), DMRGateway: filepath.Join(etc, "DMRGateway.ini"),
		YSFGateway: filepath.Join(etc, "YSFGateway.ini"), P25Gateway: filepath.Join(etc, "P25Gateway.ini"),
		NXDNGateway: filepath.Join(etc, "NXDNGateway.ini"), DStarGateway: filepath.Join(etc, "dstargateway.cfg"),
		M17Gateway: filepath.Join(etc, "M17Gateway.ini"), DAPNETGateway: filepath.Join(etc, "DAPNETGateway.ini"),
	}
	if _, err := m.WriteFiles(paths); err != nil {
		t.Fatal(err)
	}
	got, _ := os.ReadFile(paths.MMDVM)
	if string(got) != m.RenderMMDVM() {
		t.Errorf("no override dir should emit the pure render")
	}
	applied, _, err := m.Overrides(paths)
	if err != nil || len(applied) != 0 {
		t.Errorf("Overrides() with no dir = %v, %v; want empty", applied, err)
	}
}

// --- test helpers ---

func write(t *testing.T, dir, name, content string) {
	t.Helper()
	if err := os.WriteFile(filepath.Join(dir, name), []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}
}
